            <h2>Was ist urheberrechtlich geschützt?</h2>
            <p>Das Urheberrechtsgesetz schützt einerseits Werke, anderseits bestimmte sonstige Leistungen. Werke sind eigentümliche geistige Schöpfungen auf den Gebieten der Literatur, der Tonkunst, der bildenden Künste und der Filmkunst.</p>
            
            <h2>Wer entscheidet, ob eine bestimmte geistige Schöpfung urheberrechtlich geschützt ist?</h2>
            <p>Nach dem Urheberrechtsgesetz müssen geistige Schöpfungen "eigentümlich" sein, um als Werke zu gelten und damit den Schutz des Urheberrechts im engeren Sinn zu genießen. Das Tatbestandsmerkmal "eigentümlich" ist ein unbestimmter Gesetzesbegriff und bedarf daher der Auslegung durch die Rechtsprechung</p>

            <h2>Kann auch die bloße Zusammenstellung von (geschützten oder ungeschützten) Werken geschützt sein?</h2>
            <p>Ja. Neben den vier Werkkategorien Literatur, Tonkunst, bildende Künste und Filmkunst gibt es auch noch die Kategorie Sammelwerk</p>

            <h2>Welchem Schutz unterliegen Computerprogramme?</h2>
            <p>Computerprogramme sind, wenn sie das Ergebnis der eigenen geistigen Schöpfung ihres Urhebers sind, unabhängig von ihrer Ausdrucksform als Werke der Literatur urheberrechtlich geschützt.</p>

            <h2>Welche Rechte sind bei der Nutzung von Photos zu beachten?</h2>
            <p>Unter den allgemeinen Voraussetzungen ("eigentümliche geistige Schöpfung") sind Photos als Lichtbildwerke (Werkkategorie bildende Künste) durch das Urheberrecht im engeren Sinn geschütz.</p>

            <h2>Genießen auch Web-Seiten und Web-Sites urheberrechtlichen Schutz</h2>
            <p>Daß einzelne auf Web-Seiten enthaltene Inhalte (in Wort, Bild oder Ton) unter den allgemeinen Voraussetzungen ("eigentümliche geistige Schöpfung") urheberrechtlich geschützt sind, überrascht nicht. Von größerem Neuigkeitswert ist da schon die Entscheidung des Obersten Gerichtshofs, nach der auch das Layout ener Web-Seite insgesamt, und zwar als Werk der Gebrauchsgraphik und damit als Werk der bildenden Künste, urheberrechtlich geschützt sein kann</p>
    
            <h2>Kann ein minderjähriger Urheber sein?</h2>
            <p>Ja, sogar ein Kind kann Urheber sein</p>

            <h2>Wie sieht es urheberrechtlich aus, wenn zwei oder mehrere Personen an der Schaffung eines Werkes beteiligt sind?</h2>
            <p>Es kommt auf die Beiträge der einzelnen Personen an. Nur wer selbst zu der dem Werk innewohnenden Eigentümlichkeit (Individualität, Originalität) beiträgt, ist Urheber.</p>

            <h2>Muss man Urheber auch nennen?</h2>
            <p>Das Gesetz gewährt dem Urheber nicht nur vermögensrechtliche, sondern auch spezifische persönlichkeitsrechtliche Befugnisse. Zu den zweitgenannten Befugnissen, den sogenannten Urheberpersönlichkeitsrechten, zählt auch das Recht zu bestimmen, ob und mit welcher Urheberbezeichnung das Werk zu versehen ist.</p>

            <h2>Wie lange dauert das Urheberrecht?</h2>
            <p>Das Urheberrecht beginnt mit der Schaffung des Werkes und endet grundsätzlich 70 Jahre nach dem Tod des Urhebers.</p>
